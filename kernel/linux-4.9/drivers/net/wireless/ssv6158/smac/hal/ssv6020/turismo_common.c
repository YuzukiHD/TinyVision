int ssv6020_get_pa_band(int ch)
{
    int pa_band = 0;

    if (ch <= 14) {
        pa_band = BAND_2G;
    } else if (ch < 36) {
        pa_band = BAND_5100;
    } else if ((ch >= 36) && (ch < 100)) {
        pa_band = BAND_5500;
    } else if ((ch >= 100) && (ch < 140)) {
        pa_band = BAND_5700;
    } else if (ch >= 140) {
        pa_band = BAND_5900;
    }
    return pa_band;
}
