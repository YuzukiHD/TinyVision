s32 de_gamma_set_table(u32 disp, u32 en, bool is_gamma_tbl_10bit, u32 *gamma_tbl)
{
	struct de_gamma_private *priv = &(gamma_priv[disp]);
	struct gamma_reg *reg = get_gamma_reg(priv);
	int i, j;
	unsigned int *addr = NULL;
	const int TABSIZE = is_gamma_tbl_10bit ? 1024 : 256;
	const int INTP = 4;
	u32 weight[16] = { 66, 109, 66, 15,
		49, 103, 81, 23,
		34, 94,  94, 34,
		23, 81, 103, 49,};
	DE_INFO("gamma_tbl = %p, en=%d, disp=%d, is_gamma_tbl_10bit=%d\n", gamma_tbl,
		   en, disp, is_gamma_tbl_10bit);
	if (!en || gamma_tbl == NULL) {
		reg->ctl.dwval = 0x0;
		priv->set_blk_dirty(priv, GAMMA_CTL_REG_BLK, 1);
		return -1;
	}

	addr = (unsigned int *)(&(reg->tab[0]));
	reg->ctl.dwval = 0x1;

	if (is_gamma_tbl_10bit) {
		g_init_state = ENHANCE_TIGERLCD_ON;
		for (i = 0; i < TABSIZE; i++) {
			*addr = gamma_tbl[i];
			addr++;
		}
	} else {
		/*256 size 8it tab to 1024 size 10 bit tab*/
		/*linear interpolation also ok*/
		/*for (i = 0; i < TABSIZE; i++) {
			unsigned int start = gamma_tbl[i];
			unsigned int end = i < TABSIZE - 1 ? gamma_tbl[i + 1] : 0xffffff;
			unsigned int div_r = (((end & 0xff0000) >> 16) - ((start & 0xff0000) >> 16)) / INTP;
			unsigned int div_g = (((end & 0x00ff00) >> 8) - ((start & 0x00ff00) >> 8)) / INTP;
			unsigned int div_b = ((end & 0x0000ff) - (start & 0x0000ff)) / INTP;
			unsigned int start_r = (start & 0xff0000) >> 16;
			unsigned int start_g = (start & 0x00ff00) >> 8;
			unsigned int start_b = start & 0x0000ff;
			for (j = 0; j < INTP; j++) {
				*addr = ((start_r + j * div_r) << 22)
					| ((start_g + j * div_g) << 12)
					| (start_b + j * div_b) << 2;
				addr++;
			}
		}*/

		/*Gaussian interpolation*/
		for (i = 0; i < TABSIZE; i++) {
			u32 first = i > 0 ? gamma_tbl[i - 1] : 0x0;
			u32 second = gamma_tbl[i];
			u32 third = i < TABSIZE - 1 ? gamma_tbl[i + 1] : gamma_tbl[i];
			u32 fourth = i < TABSIZE - 2 ? gamma_tbl[i + 2] : gamma_tbl[i];
			u32 rgb0[3] = {0};
			u32 rgb1[3] = {0};
			u32 rgb2[3] = {0};
			u32 rgb3[3] = {0};
			u32 raw_shift[3] = {0xff0000, 0xff00, 0xff};
			u32 back_shift[3] = {16, 8, 0};
			for (j = 0; j < 3; j++) {
				/*div to rgb & 8bit to 10bit*/
				rgb0[j] = ((first & raw_shift[j]) >> back_shift[j]) * 4;
				rgb1[j] = ((second & raw_shift[j]) >> back_shift[j]) * 4;
				rgb2[j] = ((third & raw_shift[j]) >> back_shift[j]) * 4;
				rgb3[j] = ((fourth & raw_shift[j]) >> back_shift[j]) * 4;
			}
			for (j = 0; j < INTP; j++) {
				*addr = (((rgb0[0] * weight[j * INTP + 0]
					+ rgb1[0] * weight[j * INTP + 1]
					+ rgb2[0] * weight[j * INTP + 2]
					+ rgb3[0] * weight[j * INTP + 3]) / 256) << 20)
					| (((rgb0[1] * weight[j * INTP + 0]
					+ rgb1[1] * weight[j * INTP + 1]
					+ rgb2[1] * weight[j * INTP + 2]
					+ rgb3[1] * weight[j * INTP + 3]) / 256) << 10)
					| ((rgb0[2] * weight[j * INTP + 0]
					+ rgb1[2] * weight[j * INTP + 1]
					+ rgb2[2] * weight[j * INTP + 2]
					+ rgb3[2] * weight[j * INTP + 3]) / 256);
				addr++;
			}
		}
	}

	priv->set_blk_dirty(priv, GAMMA_CTL_REG_BLK, 1);
	priv->set_blk_dirty(priv, GAMMA_TAB_REG_BLK, 1);

	return 0;
}

