s32 de_gamma_get_reg_blocks(u32 disp, struct de_reg_block **blks, u32 *blk_num)
{
	struct de_gamma_private *priv = &(gamma_priv[disp]);
	u32 i, num;

	if (blks == NULL) {
		*blk_num = priv->reg_blk_num;
		DE_WARN("priv->reg_blk_num =%d\n", priv->reg_blk_num);
		return 0;
	}

	if (*blk_num >= priv->reg_blk_num) {
		num = priv->reg_blk_num;
	} else {
		num = *blk_num;
		DE_WARN("should not happen\n");
	}
	for (i = 0; i < num; ++i)
		blks[i] = priv->reg_blks + i;

	DE_WARN("blk_num =%d\n", i);
	*blk_num = i;
	return 0;
}

